package com.jmv74211.easybuy.DBInfo;


public class ProductDBInfo {

    private static ProductDBInfo instance;

    private final String COLLECTION_NAME="products";
    private final String KEY_ID = "id";
    private final String KEY_NAME = "name";
    private final String KEY_PRICE = "price";
    private final String KEY_QUANTITY = "quantity";
    private final String KEY_SECTION = "section";

    ////////////////////////////////////////////////////////////////////////////////////////////////

    private ProductDBInfo(){

    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public static ProductDBInfo getInstance(){

        if(instance == null)
            instance = new ProductDBInfo();

        return instance;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public String getCollectionName() {
        return COLLECTION_NAME;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public String getKeyID() {
        return KEY_ID;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public String getKeyName() {
        return KEY_NAME;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public String getKEY_PRICE() {
        return KEY_PRICE;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public String getKEY_QUANTITY() {
        return KEY_QUANTITY;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public String getKEY_SECTION() {
        return KEY_SECTION;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
}
